# Operadores
# Operadores aritmeticos
print("Vamos a hacer uso de los operadores aritmeticos")
print(6+1)
print(6-1)
print(6*2)
print(6/2)
print(10 % 2)
print(10 // 3)
print(2 ** 3)


# Operadores comparativos con int
print("Vamos a comparar dos int")
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)


# Operadores comparativos con String
print("Vamos a comparar dos strings")
print("Hola" > "Python")
print("Hola" < "Pyth")
print("Hola" >= "Python")  # Ordenacion alfabetica
print("Hola" <= "Python")
print("Hola" == "Hola")
print("Hola" != "Python")

# Operadores logicos
print("Vamos a mostrar resultados de usos de operadores logicos")
print(2 > 4 and 4 > 5)
print(2 < 4 or 4 > 5)
print(2 < 4 and not (4 > 5))

